''' Arithmetic Operators

Addition: (+)
Subtraction:(-)
Multiplication:(*)
Division(float):(/)
Division(floor):(//)
Modulus:(%)
Power:(**)

'''
number_one = 50
number_two = 20

print(number_one + number_two)
print(number_one - number_two)

print(number_one * number_two)
print(number_one / number_two)

print(number_one // number_two)
print(number_one % number_two)
print(number_two ** number_one)