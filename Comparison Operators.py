'''Comparision Operators

1) == or equal to
2) != or not equal to
3) > or greater than, 
4) >= or greater than or equal to,
5) < or less than, and
6) <= or less than or equal to

'''
name_one = "Nasim"
name_two = "Ahmed"

print(name_one != name_two)

num = 10
num_1 = 20

print(num <= num_1)
print(num > num_1)
print(num >= num_1)